    private void confirmInfoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmInfoMousePressed
        // TODO add your handling code here:
        int x=JOptionPane.showConfirmDialog(rootPane,"Confirm Update?");
        SimpleDateFormat df=new SimpleDateFormat("dd/MM/yyyy");
        //SimpleDateFormat df2=new SimpleDateFormat("yyyyMMdd");
        if(x==JOptionPane.YES_OPTION){
            Date d=null;
            Student s=new Student();
            try {
                d=(df.parse(dateT.getText()));
            } catch (Exception e) {
            }
            s.setDateOfBirth((java.sql.Date) new java.sql.Date(d.getTime()));
            s.setHoTen(nameT.getText());
            s.setLop(classT.getText());
            s.setGioiTinh(sexT.getText());
            s.setSdt(sdtT.getText());
            s.setEmail(emailT.getText());
            s.setAddress(diaChiT.getText());
            s.setDanToc(danTocT.getText());
            s.setTonGiao(tonGiaoT.getText());
            s.setQuocTich(quocTichT.getText());
            s.setCmnd(cmndT.getText());
            s.setNganh(nganhT.getText());
            s.setMssv(mssvT.getText());
            if(new DAO().updateStudent(s)){
                JOptionPane.showMessageDialog(rootPane, "Update Success");
                confirmInfoP.setVisible(false);
                nameL.setText(nameT.getText());
                lopL.setText(classT.getText());
                unEdit();
                
            }
            else {
                JOptionPane.showMessageDialog(rootPane, "Update fail ! Please try");
                confirmInfoP.setVisible(false);
                unEdit();
            }
        }
        else {
            unEdit();
            confirmInfoP.setVisible(false);
        }
    }//GEN-LAST:event_confirmInfoMousePressed

