void showTT_Bill(String idBill){
    SimpleDateFormat df=new SimpleDateFormat("dd/MM/yyyy");
    Bill b=new DAO().getBill_ID(idBill);
    IDBillL.setText("Số biên lai HD: "+String.valueOf(b.getIDBill()));
    hockiL.setText(b.getSemester());
    String nameRoom=new DAO().nameRoom(new DangNhap().IDUs);
    namhocL.setText(nameRoom+" - "+new DAO().zone(nameRoom));
    ngayXuatHDL.setText(df.format(b.getDateCreate()));
    collectorL.setText("Quản lý "+b.getIDStaff());
    paymentL.setText(converter(b.getPayment()));
    statusHDL.setText(b.getStatus());
    contentL.setText(b.getNoiDung());
}
