    boolean addFeedback(Feedback f){
        ArrayList<Feedback> list=new DAO().getListFeedback();
        int size=list.size();
        f.setIdFeedback(++size);
        f.setIdStudent(IDUs);
        Date d=new Date();
        f.setDateCreate(d);
        f.setEvaP(checkSao1);
        f.setEvaS(checkSao2);
        f.setEvaA(checkSao3);
        f.setContent(jTextArea1.getText());
        if(new DAO().addFeedBack(f)) return true;
        return false;
    }
